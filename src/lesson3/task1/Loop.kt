 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    var a1: Int = 1
    var a2: Int = 1
    var a3: Int = 1
    if (n == 1 || n == 2) return 1
    else for (i in 3..n) {
        a3 = a1 + a2
        a1 = a2
        a2 = a3
    }
    return a3
}

