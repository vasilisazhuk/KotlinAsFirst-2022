fun isCoPrime(m: Int, n: Int): Boolean {
    var divider = 2
    while (divider <= n && divider <= m) {
        if (n % divider == 0 && m % divider == 0) {
            return false
        }
            else {
                divider += 1
        }
    }
    return true
}

